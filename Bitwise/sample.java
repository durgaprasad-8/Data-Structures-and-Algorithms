// sample java program to explore bitmagic like XOR, OR and AND

class sample{

    public static void main(String[] args) {
        int x = 87;
        int y = 25;

        System.out.println(x ^ y);
        System.out.println(x | y);
        System.out.println(x & y);

        System.out.println(x << 2);
        System.out.println(y >> 2);
    }
}